local function effectivityblacklist(cat)

	local catlist = {}
	local effectlimitaionlist = {}
	local set = {}

	for _, c in pairs(cat) do
		catlist[c] = true
	end

	--convert recipe table into a set
	for _, recipe in pairs(data.raw.recipe) do
		if catlist[data.raw.recipe[recipe.name].category] ~= nil then
			set[recipe.name] = true
		end
	end

	for _,recipe in pairs(data.raw.recipe) do
		if set[recipe.name] == nil then
			table.insert(effectlimitaionlist, recipe.name)
		end
	end
	data.raw.module["speed-module"].limitation = effectlimitaionlist
	data.raw.module["speed-module"].limitation_message_key = 'cant be used here'
	data.raw.module["speed-module-2"].limitation = effectlimitaionlist
	data.raw.module["speed-module-2"].limitation_message_key = 'cant be used here'
	data.raw.module["speed-module-3"].limitation = effectlimitaionlist
	data.raw.module["speed-module-3"].limitation_message_key = 'cant be used here'

end

